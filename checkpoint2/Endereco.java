package checkpoint2;

//Criando a classe endereco, para fazer entrega
public class Endereco {

    private String logradouro, cidade, estado, complemento, cep;

    public Endereco(String logradouro, String cidade, String estado, String complemento, String cep) {
        this.logradouro = logradouro;
        this.cidade = cidade;
        this.estado = estado;
        this.complemento = complemento;
        this.cep = cep;
    }
}
